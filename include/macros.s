; RSTs
.macro rst_jumpTable
	rst $00
.endm

; TODO: sound control?
.macro rst_playSound
	rst $30
.endm

.MACRO ldbc
	ld bc, ((\1&$ff)<<8) | (\2&$ff)
.endm

; =======================================================================================
; Directive macros
; =======================================================================================

; =======================================================================================
; Data macros
; =======================================================================================

; dwbe = define word big endian
.macro dwbe
	.rept nargs
		.db (\1)>>8
		.db (\1)&$ff

		.shift
	.endr
.endm

; dwbe = define long word big endian
.macro dlbe
	.rept nargs
		.db (\1)>>16
		.db (\1&$ff00)>>8
		.db (\1)&$ff

		.shift
	.endr
.endm